# Integer
integer_number = 3
print("Integer = ", integer_number)

# Float
float_number = 3.45
print("Float = ", float_number)

# type()
print("Variable type = ", type(integer_number))
print("Variable type = ", type(float_number))

# Sum
sum = 45 + 2
print("45 + 2 = ", sum)

# subs
sub = 45 -25
print("45 - 25 = ", sub)

# multi
multi = 26 * 12
print("26 x 12 = ", multi)

# divide
div = 50 / 5
print("45 / 5 = ", div)
print(type(div))

# module
mod = 15 % 3
print("15 % 3 = ", mod)

# divide integer
div_Integer = 50 // 5
print("45 // 5 = ", div_Integer)
print(type(div_Integer))